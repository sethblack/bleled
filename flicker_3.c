uint8_t cycle_flicker_3(void) {
    if (CYCLE_NO > 384) {
        CYCLE_NO = 0;

        for (uint32_t n = 0; n < 512; ++n) {
            flicker3_table[n] = 255;
        }

        uint32_t FLICKER_START = (xorshift128() % 128); // random.randint(128, 256)
        uint32_t FLICKER_BOTTOM = (xorshift128() % 100) + 128; // random.randint(25, 200)
        uint32_t FLICKER_DURATION = (xorshift128() % 168) + 32; //random.randint(32, 256)
        float FLICKER_SLOPE = (float)(255 - FLICKER_BOTTOM) / (float)(FLICKER_DURATION / 2);
        uint32_t half_flicker_duration = FLICKER_DURATION / 2;

        //printf("%d %d %d %f %d\n", FLICKER_START, FLICKER_BOTTOM, FLICKER_DURATION, FLICKER_SLOPE, half_flicker_duration);

        //for f in xrange(FLICKER_DURATION / 2):
        //    X[f + FLICKER_START] = 255.0 - (FLICKER_SLOPE * f)
        for (uint8_t f = 0; f <= half_flicker_duration; ++f) {
            flicker3_table[f + FLICKER_START] = 255 - (int)(FLICKER_SLOPE * f);
        }

        //for f in xrange(FLICKER_DURATION / 2):
        //  X[f + FLICKER_START + (FLICKER_DURATION / 2)] = FLICKER_BOTTOM + (FLICKER_SLOPE * f)
        for (uint8_t f = 0; f <= half_flicker_duration; ++f) {
            flicker3_table[f + FLICKER_START + half_flicker_duration] = FLICKER_BOTTOM + (FLICKER_SLOPE * f);
        }
    }

    return flicker3_table[CYCLE_NO++];
}